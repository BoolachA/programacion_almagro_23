package edu.ucjc.programacion.herencia;

public class Padre {
	private String valor1;
	private String valor2;
	
	public Padre(String valor1, String valor2) {
		super();
		this.valor1 = valor1;
		this.valor2 = valor2;
	}
	
	public String getValor1() {
		return valor1;
	}
	
	public String getValor2() {
		return valor2;
	}
	
}
