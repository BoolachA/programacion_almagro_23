package edu.ucjc.programacion.ejercicioPoo;

public class Piston {
	/*
	 * Id
	 * Marca
	 */
	
	private String id;
	private String marca;
	
	
	public Piston(String id, String marca) {
		super();
		this.id = id;
		this.marca = marca;
	}


	public String getId() {
		return id;
	}


	public String getMarca() {
		return marca;
	}
	
	
}
