package edu.ucjc.programacion.ejercicioPoo;

public class Volante {
	/*
	 * id
	 * tipo
	 * color
	 */
	
	private String id;
	private String tipo;
	private String color;
	
	public Volante(String id, String tipo, String color) {
		super();
		this.id = id;
		this.tipo = tipo;
		this.color = color;
	}

	public String getId() {
		return id;
	}

	public String getTipo() {
		return tipo;
	}

	public String getColor() {
		return color;
	}
	
	
	
	

}
